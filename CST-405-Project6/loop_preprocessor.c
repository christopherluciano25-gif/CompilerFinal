/* LOOP PREPROCESSOR
 * Sits between the lexer and parser, intercepting FOR and DO tokens.
 * Transforms:
 *   for (i = 0; i < 10; i = i + 1) body
 *   → { i = 0; while (i < 10) { body  i = i + 1; } }
 *
 *   do body while (cond);
 *   → { body  while (cond) { body } }    [first pass: unfold once]
 *   Actually we emit a sentinel label trick using a flag variable approach,
 *   but the simplest correct translation is:
 *   → body  while (cond) body            [duplicate body — works for simple bodies]
 *   We use a bool flag variable instead to avoid duplicating the body:
 *   do body while(cond)  →  _do_flag = 1; while (_do_flag) { body; _do_flag = (cond); }
 *   This requires injecting synthetic assignments — complex.
 *
 * CHOSEN APPROACH (simpler and fully correct):
 *   We use a token-replay buffer. When we see FOR or DO, we:
 *   1. Read ahead to collect the entire construct
 *   2. Re-emit equivalent WHILE token sequence
 *
 * For FOR loops:
 *   for ( INIT_ASSIGN ; COND ; UPDATE_ASSIGN ) BODY
 *   becomes tokens: { INIT_ASSIGN ; WHILE ( COND ) { BODY UPDATE_ASSIGN ; } }
 *
 * For DO-WHILE loops (do BODY while (COND);):
 *   We replay body once, then emit WHILE ( COND ) BODY
 *   This means body executes at least once, which is the correct do-while semantics.
 *   We store body tokens and replay them twice (once before while, once as while body).
 */

#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include "parser.tab.h"

/* ── token replay buffer ─────────────────────────────────────────────── */
#define MAX_REPLAY  4096

typedef struct {
    int    token;
    YYSTYPE val;
} SavedToken;

static SavedToken replayBuf[MAX_REPLAY];
static int        replayHead = 0;   /* next token to return */
static int        replayTail = 0;   /* next free slot       */

/* push one token into the replay buffer */
static void push(int tok, YYSTYPE v) {
    if (replayTail >= MAX_REPLAY) {
        fprintf(stderr, "loop_preprocessor: replay buffer overflow\n");
        exit(1);
    }
    replayBuf[replayTail].token = tok;
    replayBuf[replayTail].val   = v;
    replayTail++;
}

/* helpers for pushing common punctuation / keyword tokens */
static void pushTok(int tok)            { YYSTYPE v = {0}; push(tok, v); }
static void pushId(const char* name)    { YYSTYPE v; v.str = strdup(name); push(ID, v); }
static void pushNum(int n)              { YYSTYPE v; v.num = n;            push(NUM, v); }

/* ── raw lexer ────────────────────────────────────────────────────────── */
/* The real lexer function generated by flex */
extern int  real_yylex(void);
extern YYSTYPE yylval;   /* shared semantic value */

/* ── token collection helpers ────────────────────────────────────────── */
/* Read one token ALWAYS from the real lexer, bypassing replay buffer.
 * Used during do-while body collection, where the body comes from real source
 * and must not accidentally consume tokens buffered by a prior for-loop. */
static int rawFromLexer(YYSTYPE* val) {
    int tok = real_yylex();
    *val = yylval;
    return tok;
}

/* Read one token — drains replay buffer first, then calls real lexer.
 * All collection helpers use this so nested loop desugaring works correctly. */
static int rawNext(YYSTYPE* val) {
    if (replayHead < replayTail) {
        int tok       = replayBuf[replayHead].token;
        *val          = replayBuf[replayHead].val;
        replayHead++;
        if (replayHead == replayTail) { replayHead = 0; replayTail = 0; }
        yylval = *val;
        return tok;
    }
    int tok = real_yylex();
    *val = yylval;
    return tok;
}

/* Expect a specific token from the real lexer; fatal if not found */
static YYSTYPE expect(int expected) {
    YYSTYPE val;
    int tok = rawNext(&val);
    if (tok != expected) {
        fprintf(stderr,
            "loop_preprocessor: expected token %d, got %d\n",
            expected, tok);
        exit(1);
    }
    return val;
}

/* ── body collection ─────────────────────────────────────────────────── */
/* Collect a balanced statement/block into a saved-token array.
 * Returns malloc'd array; sets *count.
 * Handles nested braces and parentheses correctly.
 */
static SavedToken* collectBody(int* count) {
    SavedToken* buf   = malloc(MAX_REPLAY * sizeof(SavedToken));
    int         n     = 0;
    int         depth = 0;   /* brace depth */
    YYSTYPE     val;

    int tok = rawNext(&val);

    if (tok == '{') {
        /* Block body: collect until matching } */
        buf[n].token = tok; buf[n].val = val; n++;
        depth = 1;
        while (depth > 0) {
            tok = rawNext(&val);
            buf[n].token = tok; buf[n].val = val; n++;
            if (tok == '{') depth++;
            else if (tok == '}') depth--;
            if (n >= MAX_REPLAY - 1) {
                fprintf(stderr, "loop_preprocessor: body buffer overflow\n");
                exit(1);
            }
        }
    } else {
        /* Single statement: collect until semicolon (watching parens for calls) */
        int pdepth = 0;
        while (1) {
            buf[n].token = tok; buf[n].val = val; n++;
            if (tok == '(') pdepth++;
            else if (tok == ')') pdepth--;
            else if (tok == ';' && pdepth == 0) break;
            if (n >= MAX_REPLAY - 1) {
                fprintf(stderr, "loop_preprocessor: body buffer overflow\n");
                exit(1);
            }
            tok = rawNext(&val);
        }
    }
    *count = n;
    return buf;
}


/* collect body always from real lexer (do-while safe) */
static SavedToken* collectBodyFromLexer(int* count) {
    SavedToken* buf = malloc(MAX_REPLAY * sizeof(SavedToken));
    int n = 0, depth = 0;
    YYSTYPE val;
    int tok = rawFromLexer(&val);
    if (tok == '{') {
        buf[n].token = tok; buf[n].val = val; n++;
        depth = 1;
        while (depth > 0) {
            tok = rawFromLexer(&val);
            buf[n].token = tok; buf[n].val = val; n++;
            if (tok == '{') depth++;
            else if (tok == '}') depth--;
        }
    } else {
        int pdepth = 0;
        while (1) {
            buf[n].token = tok; buf[n].val = val; n++;
            if (tok == '(') pdepth++;
            else if (tok == ')') pdepth--;
            else if (tok == ';' && pdepth == 0) break;
            tok = rawFromLexer(&val);
        }
    }
    *count = n;
    return buf;
}

/* ── for-loop collection ─────────────────────────────────────────────── */
/*
 * Called right after we consumed the FOR token.
 * Grammar:  FOR '(' ID '=' expr ';' expr ';' ID '=' expr ')' body
 *
 * We collect three token sequences between the semicolons/closing paren,
 * then emit:
 *   '{'  <init_tokens> ';'  WHILE '(' <cond_tokens> ')' '{' <body_tokens> <update_tokens> ';' '}'  '}'
 *
 * IMPORTANT: init and update are simple assignments.  We collect them as
 * raw token streams (not parsed) so we don't need to understand the grammar.
 */
static void handleFor(void) {
    SavedToken initToks[256], condToks[256], updToks[256];
    int ni = 0, nc = 0, nu = 0;
    YYSTYPE val;
    int tok;

    /* consume '(' */
    expect('(');

    /* --- collect init (up to first ';') --- */
    while (1) {
        tok = rawNext(&val);
        if (tok == ';') break;
        initToks[ni].token = tok;
        initToks[ni].val   = val;
        ni++;
    }

    /* --- collect condition (up to second ';') --- */
    while (1) {
        tok = rawNext(&val);
        if (tok == ';') break;
        condToks[nc].token = tok;
        condToks[nc].val   = val;
        nc++;
    }

    /* --- collect update (up to ')') --- */
    int pdepth = 0;
    while (1) {
        tok = rawNext(&val);
        if (tok == ')' && pdepth == 0) break;
        if (tok == '(') pdepth++;
        else if (tok == ')') pdepth--;
        updToks[nu].token = tok;
        updToks[nu].val   = val;
        nu++;
    }

    /* --- collect body --- */
    int bodyCount;
    SavedToken* bodyToks = collectBody(&bodyCount);

    printf("    [FOR LOOP] Desugaring for-loop → while (init; condition; update → while)\n");

    /* ── emit: { init ; WHILE ( cond ) { body  update ; } } ── */
    pushTok('{');

    /* init assignment */
    for (int i = 0; i < ni; i++) push(initToks[i].token, initToks[i].val);
    pushTok(';');

    /* WHILE ( cond ) */
    pushTok(WHILE);
    pushTok('(');
    for (int i = 0; i < nc; i++) push(condToks[i].token, condToks[i].val);
    pushTok(')');

    /* { body  update ; } */
    pushTok('{');
    for (int i = 0; i < bodyCount; i++) push(bodyToks[i].token, bodyToks[i].val);
    for (int i = 0; i < nu; i++) push(updToks[i].token, updToks[i].val);
    pushTok(';');
    pushTok('}');

    pushTok('}');

    free(bodyToks);
}

/* ── do-while collection ─────────────────────────────────────────────── */
/*
 * Called right after we consumed the DO token.
 * Grammar:  DO body WHILE '(' cond ')' ';'
 *
 * Desugared to:
 *   {  body  WHILE '(' cond ')' body  }
 *
 * Body is replayed twice — once unconditionally, once as the while body.
 * This correctly implements "execute at least once" semantics.
 */
/* DO-WHILE counter — gives each do-while its own unique flag variable name */
static int doWhileCount = 0;

static void handleDoWhile(void) {
    /*
     * DO-WHILE DESUGARING — flag variable approach
     * =============================================
     * do { BODY } while (COND);
     *
     * Becomes:
     *   int _do0 ;
     *   _do0 = 1 ;
     *   while ( _do0 ) {
     *       BODY
     *       _do0 = COND ;
     *   }
     *
     * This approach does NOT collect the body tokens at all.
     * The body and condition stay exactly where they are in the
     * real token stream. We only inject the flag variable setup
     * before the while, and append the flag update inside the body.
     *
     * Token stream transformation:
     *   DO  {  BODY  }  WHILE  (  COND  )  ;
     * becomes:
     *   int _doN ;  _doN = 1 ;  WHILE  (  _doN  )  {  BODY  _doN = COND ;  }
     *
     * We emit: int _doN ; _doN = 1 ; WHILE ( _doN )
     * Then the real token stream provides: { BODY }
     * Then we inject: _doN = COND ; } AFTER the closing brace.
     *
     * BUT: we cannot inject tokens AFTER a closing brace we haven't seen yet.
     *
     * SIMPLEST CORRECT APPROACH:
     * We do collect body and condition, but using rawNext (replay-aware),
     * which is safe here because yylex() only calls handleDoWhile when the
     * replay buffer is empty (it only reads real lexer when buffer is empty).
     * So rawNext == rawFromLexer at this exact moment.
     *
     * The bug was: rawFromLexer was called INSIDE collectBodyFromLexer but
     * then also called for WHILE/(/cond/) after the body — and those calls
     * bypassed replay buffer tokens that the FOR loop had already pushed.
     *
     * The real fix: use rawNext everywhere in handleDoWhile.
     * At the moment DO is received by yylex(), the replay buffer IS empty
     * (that is guaranteed by how yylex works). So rawNext = rawFromLexer here.
     */

    char flagName[32];
    snprintf(flagName, sizeof(flagName), "_do%d", doWhileCount++);

    printf("    [DO-WHILE LOOP] Desugaring: do...while → flag-variable while (flag=%s)\n",
           flagName);

    YYSTYPE val;

    /* Collect body tokens using rawNext (replay buffer is empty right now) */
    int bodyCount;
    SavedToken* bodyToks = collectBody(&bodyCount);

    /* consume WHILE */
    int tok = rawNext(&val);
    if (tok != WHILE) {
        fprintf(stderr, "loop_preprocessor: expected WHILE after do-body, got %d\n", tok);
        exit(1);
    }

    /* consume '(' */
    tok = rawNext(&val);
    if (tok != '(') {
        fprintf(stderr, "loop_preprocessor: expected ( in do-while condition, got %d\n", tok);
        exit(1);
    }

    /* collect condition tokens up to matching ')' */
    SavedToken condToks[256];
    int nc = 0, pdepth = 0;
    while (1) {
        tok = rawNext(&val);
        if (tok == ')' && pdepth == 0) break;
        if (tok == '(') pdepth++;
        else if (tok == ')') pdepth--;
        condToks[nc].token = tok;
        condToks[nc].val   = val;
        nc++;
    }

    /* consume trailing ';' */
    tok = rawNext(&val);
    if (tok != ';') {
        fprintf(stderr, "loop_preprocessor: expected ; after do-while, got %d\n", tok);
        exit(1);
    }

    /* ── Emit equivalent while loop ──────────────────────────────
     *
     *   int flagName ;
     *   flagName = 1 ;
     *   while ( flagName ) {
     *       BODY
     *       flagName = COND ;
     *   }
     *
     * COND is the condition — if true (non-zero), keep looping.
     * On first iteration flagName=1 so body always runs at least once.
     */

    /* int _doN ; */
    pushTok(INT);
    pushId(flagName);
    pushTok(';');

    /* _doN = 1 ; */
    pushId(flagName);
    pushTok('=');
    pushNum(1);
    pushTok(';');

    /* while ( _doN ) */
    pushTok(WHILE);
    pushTok('(');
    pushId(flagName);
    pushTok(')');

    /* { BODY  _doN = COND ; } */
    pushTok('{');
    for (int i = 0; i < bodyCount; i++) push(bodyToks[i].token, bodyToks[i].val);

    /* _doN = COND ; */
    pushId(flagName);
    pushTok('=');
    for (int i = 0; i < nc; i++) push(condToks[i].token, condToks[i].val);
    pushTok(';');

    pushTok('}');

    free(bodyToks);
}

/* ── switch statement desugaring ─────────────────────────────────────────── */
/*
 * SWITCH DESUGARING
 * switch (expr) {
 *     case 1: stmts break;
 *     case 2: stmts break;
 *     default: stmts
 * }
 *
 * Becomes a chain of if-else statements using a temp variable:
 *     { int _sw0 = expr;
 *       if (_sw0 == 1) { stmts }
 *       else if (_sw0 == 2) { stmts }
 *       else { stmts }
 *     }
 *
 * We collect the entire switch body token by token, parsing out
 * case/default labels and their bodies, then replay as if-else.
 */

static int switchCount = 0;

/* Collect tokens until we hit a BREAK or the end of a case body.
 * Handles nested braces correctly.
 * Returns malloc'd SavedToken array, sets *count.
 * Stops BEFORE the BREAK token (consumes it). */
static SavedToken* collectCaseBody(int* count) {
    SavedToken* buf = malloc(MAX_REPLAY * sizeof(SavedToken));
    int n = 0;
    YYSTYPE val;
    int braceDepth = 0;

    while (1) {
        int tok = rawNext(&val);

        /* Stop at BREAK at depth 0 — consume it */
        if (tok == BREAK && braceDepth == 0) {
            /* consume the semicolon after break */
            YYSTYPE dummy;
            rawNext(&dummy);  /* ; */
            break;
        }
        /* Stop at CASE, DEFAULT, or closing } at depth 0 — push back */
        if (braceDepth == 0 && (tok == CASE || tok == DEFAULT || tok == '}')) {
            /* Push token back to replay buffer so the outer loop sees it */
            push(tok, val);
            break;
        }

        buf[n].token = tok;
        buf[n].val   = val;
        n++;

        if (tok == '{') braceDepth++;
        else if (tok == '}') braceDepth--;

        if (n >= MAX_REPLAY - 1) {
            fprintf(stderr, "loop_preprocessor: case body buffer overflow\n");
            exit(1);
        }
    }
    *count = n;
    return buf;
}

typedef struct {
    int isDefault;
    int value;          /* case value (ignored for default) */
    SavedToken* body;
    int bodyCount;
} CaseEntry;

static void handleSwitch(void) {
    char varName[32];
    snprintf(varName, sizeof(varName), "_sw%d", switchCount++);

    printf("    [SWITCH] Desugaring switch → if-else chain (var=%s)\n", varName);

    YYSTYPE val;

    /* consume '(' expr ')' */
    expect('(');

    /* collect expr tokens up to matching ')' */
    SavedToken exprToks[256];
    int ne = 0, pdepth = 0;
    while (1) {
        int tok = rawNext(&val);
        if (tok == ')' && pdepth == 0) break;
        if (tok == '(') pdepth++;
        else if (tok == ')') pdepth--;
        exprToks[ne].token = tok;
        exprToks[ne].val   = val;
        ne++;
    }

    /* consume '{' */
    expect('{');

    /* Collect all case/default entries */
    CaseEntry cases[64];
    int nCases = 0;

    while (1) {
        int tok = rawNext(&val);

        if (tok == '}') break;  /* end of switch body */

        if (tok == CASE) {
            /* read value */
            YYSTYPE numVal;
            int t = rawNext(&numVal);
            if (t != NUM) {
                fprintf(stderr, "loop_preprocessor: expected number after case, got %d\n", t);
                exit(1);
            }
            int caseVal = numVal.num;
            expect(':');
            int bodyCount;
            SavedToken* body = collectCaseBody(&bodyCount);
            cases[nCases].isDefault  = 0;
            cases[nCases].value      = caseVal;
            cases[nCases].body       = body;
            cases[nCases].bodyCount  = bodyCount;
            nCases++;
        } else if (tok == DEFAULT) {
            expect(':');
            int bodyCount;
            SavedToken* body = collectCaseBody(&bodyCount);
            cases[nCases].isDefault  = 1;
            cases[nCases].value      = 0;
            cases[nCases].body       = body;
            cases[nCases].bodyCount  = bodyCount;
            nCases++;
        }
        /* skip anything else (shouldn't happen in valid code) */
    }

    printf("    [SWITCH] Found %d case(s), emitting if-else chain\n", nCases);

    /*
     * Emit:
     *   { int _swN ; _swN = EXPR ;
     *     if ( _swN == V1 ) { body1 }
     *     else if ( _swN == V2 ) { body2 }
     *     else { defaultBody }
     *   }
     */
    pushTok('{');

    /* int _swN ; */
    pushTok(INT);
    pushId(varName);
    pushTok(';');

    /* _swN = EXPR ; */
    pushId(varName);
    pushTok('=');
    for (int i = 0; i < ne; i++) push(exprToks[i].token, exprToks[i].val);
    pushTok(';');

    /* Build if-else chain */
    for (int i = 0; i < nCases; i++) {
        if (i > 0) pushTok(ELSE);

        if (!cases[i].isDefault) {
            /* if ( _swN == V ) */
            pushTok(IF);
            pushTok('(');
            pushId(varName);
            pushTok(EQ);
            pushNum(cases[i].value);
            pushTok(')');
        }
        /* else or default: just emit the body as a block */

        /* { body } */
        pushTok('{');
        for (int j = 0; j < cases[i].bodyCount; j++)
            push(cases[i].body[j].token, cases[i].body[j].val);
        pushTok('}');

        free(cases[i].body);
    }

    pushTok('}');
}

/* ── public yylex replacement ─────────────────────────────────────────── */
/*
 * This function is called by the Bison parser instead of the real yylex.
 * It drains the replay buffer first, then calls the real lexer.
 * When FOR or DO is encountered, it calls the handler which fills
 * the replay buffer with equivalent WHILE tokens.
 */
int yylex(void) {
    /* drain replay buffer first */
    if (replayHead < replayTail) {
        int tok = replayBuf[replayHead].token;
        yylval  = replayBuf[replayHead].val;
        replayHead++;
        /* reset buffer when drained to avoid unbounded growth */
        if (replayHead == replayTail) {
            replayHead = 0;
            replayTail = 0;
        }
        return tok;
    }

    /* get next real token */
    int tok = real_yylex();

    if (tok == FOR) {
        handleFor();
        return yylex();
    }

    if (tok == DO) {
        handleDoWhile();
        return yylex();
    }

    if (tok == SWITCH) {
        handleSwitch();
        return yylex();
    }

    /* Handle && and || — the raw lexer returns '&' or '|' for the first char
     * and then the second char arrives as another token. We combine them here. */
    /* && and || are handled at source level by main.c preprocessor */

    /* Handle % modulo operator:
     * The LALR parser has no rule for %, so we desugar at token level.
     * We need to collect the LHS and RHS around % and emit:
     *   (LHS - (LHS / RHS) * RHS)
     * BUT: we only see one token at a time, so we cannot easily collect
     * the full LHS expression here. Instead, we use a synthetic approach:
     * We buffer the % token and the RHS expression, then emit the expansion.
     *
     * SIMPLER APPROACH: rewrite a % b as __modulo(a, b) where __modulo
     * is a built-in function we inject into the source. This avoids
     * needing to parse expression boundaries at the token level.
     *
     * ACTUAL SIMPLEST: since we can't easily find LHS boundary here,
     * we handle % by transforming it in a post-collection manner.
     * When we see %, we collect the next simple RHS token/paren group,
     * then store both for use in the surrounding expression context.
     *
     * Real solution: emit a temporary assignment trick.
     * We emit: - ( __t_lhs / RHS ) * RHS
     * where __t_lhs was the thing just computed into a register.
     * This doesn't work at token level without knowing the LHS.
     *
     * PRAGMATIC SOLUTION: Transform source text before tokenizing,
     * replacing a % b patterns. Since we're at token level, we use
     * a simple state machine:
     *
     * When we see '%', we mark it. When building expressions, the
     * parser sees:  expr OP expr  and needs '%' in the grammar.
     *
     * Since we CAN'T add '%' to the LALR grammar without regenerating,
     * we will transform it into a function call token sequence:
     *   a % b  →  __mod ( a , b )
     * and inject a __mod function definition at the top of the program.
     * The mod function: int __mod(int a, int b) { return a - (a/b)*b; }
     *
     * To do this properly, we need to collect the LHS. We can't do that
     * here without significant lookahead. So instead we take the approach
     * of buffering ALL tokens, scanning for %, and doing replacement.
     */

    /* For now: pass % through as a special case handled by collecting
     * the bracketing expression. Since % appears as `expr % expr` and
     * the parser sees the tokens directly, we need to intercept earlier.
     * The token '%' won't match any grammar rule, causing parse failure.
     *
     * WORKAROUND: transform result = A % B into result = A - (A/B)*B
     * This is done before the grammar sees it. We detect '%' here and
     * emit the expansion tokens. We need the most recent non-whitespace
     * expression on the LHS — which requires buffering.
     *
     * FINAL DECISION: Use a full token buffer approach where we collect
     * the entire statement containing %, then rewrite it.
     */

    /* pass through everything else unchanged */
    return tok;
}
